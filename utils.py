# ==========================================
# 终端颜色常量定义区
# 【为什么这么做？】
# 将复杂的 ANSI 转义序列封装成简单易读的英文单词。
# 以后在代码里直接写 RED + "文字" + RESET 就能变色，大大提高代码可读性。
# ==========================================
RED = '\033[91m'      # 亮红色，用于报错或对比出“不同”的内容
GREEN = '\033[92m'    # 亮绿色，用于成功提示或对比出“完全相同”的内容
YELLOW = '\033[93m'   # 亮黄色，用于警告、提示信息或“部分相同”的内容
CYAN = '\033[96m'     # 青色，用于系统运行状态的引导提示，与业务结果区分开
RESET = '\033[0m'     # 重置颜色，非常重要！变色完必须加这个，否则后面的字全跟着变色

def get_display_width(text):
    """
    【作用】精确计算字符串在中文终端下的物理显示宽度。
    【为什么这么做？】
    Python 内置的 len() 函数算的是“字符数”。在它眼里，"A" 的长度是 1，"字" 的长度也是 1。
    但在终端屏幕上，一个汉字（全角字符）占用的物理宽度是两个英文字母（半角字符）的宽度！
    如果不解决这个问题，只要表格里混了中英文，竖线绝对无法对齐。
    """
    width = 0
    for char in str(text):
        # 使用 unicodedata 库判断该字符在东亚排版中的属性
        ea = unicodedata.east_asian_width(char)
        # 'W'(Wide 宽字符), 'F'(Fullwidth 全角), 'A'(Ambiguous 模糊，在中文终端默认视同宽字符)
        if ea in ('W', 'F', 'A'):
            width += 2 # 汉字或全角标点，物理宽度算作 2
        else:
            width += 1 # 英文字母、数字或半角标点，物理宽度算作 1
    return width

def pad_string(text, target_width):
    """
    【作用】根据给定的目标宽度，自动在字符串后面补齐相应数量的空格。
    【为什么这么做？】
    因为我们要画表格。比如这一列我们规定宽度是 20，你的文字算出来宽度只有 15，
    那剩下的 5 个空位就必须由这个函数塞入 5 个真实的空格，这样画出来的右边框 '|' 才能笔直对齐。
    """
    text = str(text)
    # 调用上面的函数，算出这个字符串在屏幕上到底占多宽
    current_width = get_display_width(text)
    # 算出差额。使用 max(0, ...) 是为了防止文字超长导致算出负数报错
    padding = max(0, target_width - current_width)
    # 将原文字加上算好数量的空格，拼成一个完整的字符串返回
    return text + " " * padding
